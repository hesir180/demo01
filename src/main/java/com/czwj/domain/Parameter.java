package com.czwj.domain;

public class Parameter {
	private int id;
	private String parameterCode;
	private String parameterName;
	private double parameterRatio;
	private String parameterMeter;
	private String meterModelCode;
	private String meterModelName;
	private String energyCode;
	private String energySourceClass;
	private String realMvCode;
	private String realMvName;
	private String meterType;
	private String parameterRemark;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getParameterCode() {
		return parameterCode;
	}
	public void setParameterCode(String parameterCode) {
		this.parameterCode = parameterCode;
	}
	public String getParameterName() {
		return parameterName;
	}
	public void setParameterName(String parameterName) {
		this.parameterName = parameterName;
	}
	public double getParameterRatio() {
		return parameterRatio;
	}
	public void setParameterRatio(double parameterRatio) {
		this.parameterRatio = parameterRatio;
	}
	public String getParameterMeter() {
		return parameterMeter;
	}
	public void setParameterMeter(String parameterMeter) {
		this.parameterMeter = parameterMeter;
	}
	public String getMeterModelCode() {
		return meterModelCode;
	}
	public void setMeterModelCode(String meterModelCode) {
		this.meterModelCode = meterModelCode;
	}
	public String getMeterModelName() {
		return meterModelName;
	}
	public void setMeterModelName(String meterModelName) {
		this.meterModelName = meterModelName;
	}
	public String getEnergyCode() {
		return energyCode;
	}
	public void setEnergyCode(String energyCode) {
		this.energyCode = energyCode;
	}
	public String getEnergySourceClass() {
		return energySourceClass;
	}
	public void setEnergySourceClass(String energySourceClass) {
		this.energySourceClass = energySourceClass;
	}
	public String getRealMvCode() {
		return realMvCode;
	}
	public void setRealMvCode(String realMvCode) {
		this.realMvCode = realMvCode;
	}
	public String getRealMvName() {
		return realMvName;
	}
	public void setRealMvName(String realMvName) {
		this.realMvName = realMvName;
	}
	public String getMeterType() {
		return meterType;
	}
	public void setMeterType(String meterType) {
		this.meterType = meterType;
	}
	public String getParameterRemark() {
		return parameterRemark;
	}
	public void setParameterRemark(String parameterRemark) {
		this.parameterRemark = parameterRemark;
	}
	
}
