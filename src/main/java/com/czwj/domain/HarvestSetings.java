package com.czwj.domain;

public class HarvestSetings {
	private int id;
	private String entityCode;
	private String entityName;
	private String harvestCode;
	private String harvestName;
	private String harvestUnit;
	private String remark;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getEntityCode() {
		return entityCode;
	}
	public void setEntityCode(String entityCode) {
		this.entityCode = entityCode;
	}
	public String getEntityName() {
		return entityName;
	}
	public void setEntityName(String entityName) {
		this.entityName = entityName;
	}
	public String getHarvestCode() {
		return harvestCode;
	}
	public void setHarvestCode(String harvestCode) {
		this.harvestCode = harvestCode;
	}
	public String getHarvestName() {
		return harvestName;
	}
	public void setHarvestName(String harvestName) {
		this.harvestName = harvestName;
	}
	public String getHarvestUnit() {
		return harvestUnit;
	}
	public void setHarvestUnit(String harvestUnit) {
		this.harvestUnit = harvestUnit;
	}
	public String getRemark() {
		return remark;
	}
	public void setRemark(String remark) {
		this.remark = remark;
	}
	
}
