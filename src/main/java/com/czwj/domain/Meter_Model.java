package com.czwj.domain;

public class Meter_Model {
	private int id;
	private String mmCode;
	private String mmName;
	private String mmUpCode;
	private String mmUpName;
	private String mmIsSection;
	private String mmEnergyId;
	private String mmEnergyName;
	private String remark;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getMmCode() {
		return mmCode;
	}
	public void setMmCode(String mmCode) {
		this.mmCode = mmCode;
	}
	public String getMmName() {
		return mmName;
	}
	public void setMmName(String mmName) {
		this.mmName = mmName;
	}
	public String getMmUpCode() {
		return mmUpCode;
	}
	public void setMmUpCode(String mmUpCode) {
		this.mmUpCode = mmUpCode;
	}
	public String getMmUpName() {
		return mmUpName;
	}
	public void setMmUpName(String mmUpName) {
		this.mmUpName = mmUpName;
	}
	public String getMmIsSection() {
		return mmIsSection;
	}
	public void setMmIsSection(String mmIsSection) {
		this.mmIsSection = mmIsSection;
	}
	public String getMmEnergyId() {
		return mmEnergyId;
	}
	public void setMmEnergyId(String mmEnergyId) {
		this.mmEnergyId = mmEnergyId;
	}
	public String getMmEnergyName() {
		return mmEnergyName;
	}
	public void setMmEnergyName(String mmEnergyName) {
		this.mmEnergyName = mmEnergyName;
	}
	public String getRemark() {
		return remark;
	}
	public void setRemark(String remark) {
		this.remark = remark;
	}
	
}
