package com.czwj.domain;

public class Harvest_Values {
	private int id;
	private String entityCode;
	private String entityName;
	private String harvestVCode;
	private String harvestVName;
	private String harvestVUnit;
	private String remark;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getEntityCode() {
		return entityCode;
	}
	public void setEntityCode(String entityCode) {
		this.entityCode = entityCode;
	}
	public String getEntityName() {
		return entityName;
	}
	public void setEntityName(String entityName) {
		this.entityName = entityName;
	}
	public String getHarvestVCode() {
		return harvestVCode;
	}
	public void setHarvestVCode(String harvestVCode) {
		this.harvestVCode = harvestVCode;
	}
	public String getHarvestVName() {
		return harvestVName;
	}
	public void setHarvestVName(String harvestVName) {
		this.harvestVName = harvestVName;
	}
	public String getHarvestVUnit() {
		return harvestVUnit;
	}
	public void setHarvestVUnit(String harvestVUnit) {
		this.harvestVUnit = harvestVUnit;
	}
	public String getRemark() {
		return remark;
	}
	public void setRemark(String remark) {
		this.remark = remark;
	}
	
}
