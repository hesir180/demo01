package com.czwj.domain;

public class Electro_Quality_Variable {
	private int id;
	private String quality_Code;
	private String quality_Name;
	private String remark;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getQuality_Code() {
		return quality_Code;
	}
	public void setQuality_Code(String quality_Code) {
		this.quality_Code = quality_Code;
	}
	public String getQuality_Name() {
		return quality_Name;
	}
	public void setQuality_Name(String quality_Name) {
		this.quality_Name = quality_Name;
	}
	public String getRemark() {
		return remark;
	}
	public void setRemark(String remark) {
		this.remark = remark;
	}
	
}
