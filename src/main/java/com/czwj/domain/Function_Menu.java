package com.czwj.domain;

public class Function_Menu {
	private int id;
	private String menuId;
	private String menuName;
	private String parentId;
	private String menuUrl;
	private String iconPath;
	private String iconOpenPath;
	private String iconClosePath;
	private String menuStatus;
	private String defaultSelect;
	private String chartSelect;
	private String remark;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getMenuId() {
		return menuId;
	}
	public void setMenuId(String menuId) {
		this.menuId = menuId;
	}
	public String getMenuName() {
		return menuName;
	}
	public void setMenuName(String menuName) {
		this.menuName = menuName;
	}
	public String getParentId() {
		return parentId;
	}
	public void setParentId(String parentId) {
		this.parentId = parentId;
	}
	public String getMenuUrl() {
		return menuUrl;
	}
	public void setMenuUrl(String menuUrl) {
		this.menuUrl = menuUrl;
	}
	public String getIconPath() {
		return iconPath;
	}
	public void setIconPath(String iconPath) {
		this.iconPath = iconPath;
	}
	public String getIconOpenPath() {
		return iconOpenPath;
	}
	public void setIconOpenPath(String iconOpenPath) {
		this.iconOpenPath = iconOpenPath;
	}
	public String getIconClosePath() {
		return iconClosePath;
	}
	public void setIconClosePath(String iconClosePath) {
		this.iconClosePath = iconClosePath;
	}
	public String getMenuStatus() {
		return menuStatus;
	}
	public void setMenuStatus(String menuStatus) {
		this.menuStatus = menuStatus;
	}
	public String getDefaultSelect() {
		return defaultSelect;
	}
	public void setDefaultSelect(String defaultSelect) {
		this.defaultSelect = defaultSelect;
	}
	public String getChartSelect() {
		return chartSelect;
	}
	public void setChartSelect(String chartSelect) {
		this.chartSelect = chartSelect;
	}
	public String getRemark() {
		return remark;
	}
	public void setRemark(String remark) {
		this.remark = remark;
	}
	
}
